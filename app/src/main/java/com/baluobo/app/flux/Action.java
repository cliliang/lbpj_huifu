public class Action<T> {
    private String type;
    private T data;

    public Action(String type, T data) {
        this.type = type;
        this.data = data;
    }

    public Action(String type){
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public T getData() {
        return data;
    }
}
