public class ExpGood {
    private int expGoodId;
    private int investTime;
    private float proceeds;
    private int goodMoney;
    private int goodFlg;  // 0未购买 1已购买

    public int getExpGoodId() {
        return expGoodId;
    }

    public void setExpGoodId(int expGoodId) {
        this.expGoodId = expGoodId;
    }

    public int getInvestTime() {
        return investTime;
    }

    public void setInvestTime(int investTime) {
        this.investTime = investTime;
    }

    public float getProceeds() {
        return proceeds;
    }

    public void setProceeds(float proceeds) {
        this.proceeds = proceeds;
    }

    public int getGoodMoney() {
        return goodMoney;
    }

    public void setGoodMoney(int goodMoney) {
        this.goodMoney = goodMoney;
    }

    public int getGoodFlg() {
        return goodFlg;
    }

    public void setGoodFlg(int goodFlg) {
        this.goodFlg = goodFlg;
    }
}
